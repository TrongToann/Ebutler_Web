package com.ebutler.swp.dto;

/**
 *
 * @author Admin
 */
public class ServiceErrorDTO {
    private String name;
    private String price;

    public ServiceErrorDTO() {
        this.name = "";
        this.price = "";
    }

    public ServiceErrorDTO(String name, String price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
    
    
}
