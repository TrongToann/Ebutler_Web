package com.ebutler.swp.dto;

/**
 *
 * @author Admin
 */
public class ProvinceDTO {
    private String id;
    private String name;

    public ProvinceDTO() {
        this.id = "";
        this.name = "";
    }  

    public ProvinceDTO(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
